#include <iostream>
#include <vector>

int main() {
    int n;
    std::cin >> n;

    std::vector<int> dp(n + 1);
    std::vector<int> dp2(n + 1);
    dp[1] = 1, dp2[1] = 0;
    dp[2] = 3, dp2[2] = 2;
    dp[3] = 5, dp2[3] = 2;

    for (int i = 4; i <= n; ++i) {
        dp[i] = dp[i - 1] + dp[i - 2] * 2;
        dp[i] %= 10007;
    }
    std::cout << dp[n];
}

